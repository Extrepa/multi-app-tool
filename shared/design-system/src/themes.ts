/**
 * Get theme colors by theme name
 */
export function getThemeColors(themeName: string): ThemeColors | null {
  return THEME_LAB_THEMES[themeName] || null;
}

/**
 * List all available theme names
 */
export function getThemeNames(): string[] {
  return Object.keys(THEME_LAB_THEMES);
}

/**
 * Check if theme exists
 */
export function hasTheme(themeName: string): boolean {
  return themeName in THEME_LAB_THEMES;
}
